const express = require('express')
const router = express.Router()

/************************************************************/
/************************************************************/

/*****************/
/***** INDEX *****/
/*****************/

router.get('/', function(req, res, next) {
  res.send('search')
})

/************************************************************/
/************************************************************/

/****************/
/***** WORD *****/
/****************/

router.get('/:word', function(req, res, next) {
  res.send(`search for ${req.params.word}`)
})

/************************************************************/
/************************************************************/

/*******************/
/***** EXPORTS *****/
/*******************/

module.exports = router
